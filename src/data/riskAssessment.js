export const getRiskProfile = (score) => riskProfiles.find((profile) => score >= profile.min && score <= profile.max) || riskProfiles[0]

export const getDimensions = (answers) => {
  const totals = { volatility: [], independence: [], discipline: [] }
  riskQuestions.forEach((question, index) => {
    if (answers[index]) totals[question.dimension].push(answers[index])
  })
  return Object.fromEntries(Object.entries(totals).map(([key, values]) => {
    const average = values.length ? values.reduce((sum, value) => sum + value, 0) / values.length : 1
    return [key, Math.round(((average - 1) / 3) * 100)]
  }))
}
